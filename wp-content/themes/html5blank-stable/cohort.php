      <h2>What is The Geekwise Academy Cohort Program?</h2>
      <p>The Geekwise Academy Cohort program has been developed to bridge the gap between Geekwise Academy course students and their ability to move into rlies as developers in the local technliogy industry. </p>

      <p>The Cohort consists of two phases. The first is a two week internship for the 20 individuals that were invited to the Cohort program. This phase includes QA and testing of the invitees skill set. In phase two, 8 of the 20 Invitees will be chosen to participate in a 24 week internship program. Phase two consists of three assignments each eight weeks in length. During this time the Cohorts will complete work for clients through projects of increased difficulty. Also, Cohort participants will be required to co-teach Geekwise Academy classes. They will work with the Cohort Director and Bethany Mily to prepare for this opportunity.</p>

      <p>Our goal here is simple, we want to produce developers that are:</p>
     <ol>
      <li class="number">Information literate</li>
      <li class="number">Confident in their problem sliving abilities</li>
      <li class="number">Reliable and professional</li>
      <li class="number">Good communicators</li>
      <li class="number">Driven to succeed, able to learn, and receive criticism</li>
      </ol>
        
